lanche = ('hamburguer', 'suco', 'pizza', 'pudim')

print(lanche)
print(lanche[2]) # mostra o item 2 (pizza)
print(lanche[0:2]) # o último elemento é ignorado
print(lanche[1:]) # vai do suco até o final 
print(lanche[-1]) # mostra o ultimo elemento

print(len(lanche)) # mostra quantos elementos tem a tupla